# 3. Domain-Aware Missing Value Handling

# Based on data description, `NA` often means "absence" (e.g., no basement, no garage, no pool).

# Define groups of features with specific imputation strategies

# Features where NA means "None" or "No" (categorical)
none_cats = ['Alley', 'BsmtQual', 'BsmtCond', 'BsmtExposure', 'BsmtFinType1',
             'BsmtFinType2', 'FireplaceQu', 'GarageType', 'GarageFinish',
             'GarageQual', 'GarageCond', 'PoolQC', 'Fence', 'MiscFeature', 'KitchenQual']

# For these, fill with 'None' (or 'No')
for col in none_cats:
    if col in all_data.columns:
        all_data[col] = all_data[col].fillna('None')

# Numerical features where NA means 0 (absence)
zero_num = ['MasVnrArea', 'BsmtFinSF1', 'BsmtFinSF2', 'BsmtUnfSF', 'TotalBsmtSF',
            'BsmtFullBath', 'BsmtHalfBath', 'GarageArea', 'GarageCars',
            'WoodDeckSF', 'OpenPorchSF', 'EnclosedPorch', '3SsnPorch',
            'ScreenPorch', 'PoolArea', 'MiscVal']

for col in zero_num:
    if col in all_data.columns:
        all_data[col] = all_data[col].fillna(0)

# LotFrontage: fill with median per Neighborhood (strong correlation)
if 'LotFrontage' in all_data.columns:
    all_data['LotFrontage'] = all_data.groupby('Neighborhood')['LotFrontage'].transform(
        lambda x: x.fillna(x.median())
    )
    # If still NaN (Neighborhood with all NaN), use global median
    all_data['LotFrontage'] = all_data['LotFrontage'].fillna(all_data['LotFrontage'].median())

# MasVnrType: fill with 'None' (matches zero area)
if 'MasVnrType' in all_data.columns:
    all_data['MasVnrType'] = all_data['MasVnrType'].fillna('None')

# Electrical: only one missing in training set, fill with mode
if 'Electrical' in all_data.columns:
    all_data['Electrical'] = all_data['Electrical'].fillna(all_data['Electrical'].mode()[0])

# GarageYrBlt: if missing, assume no garage -> 0
