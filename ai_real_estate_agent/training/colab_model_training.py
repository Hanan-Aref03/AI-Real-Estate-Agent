print(f"Shape of X_test_final (for submission): {X_test_final.shape}")

# -----------------------------------------------------------------------------
# 4. PREPROCESSING PIPELINE (FIT ONLY ON TRAIN)
# -----------------------------------------------------------------------------
# Identify columns by type from the actual training data (X_train)
numeric_cols = X_train.select_dtypes(include=np.number).columns.tolist()
categorical_cols = X_train.select_dtypes(include='object').columns.tolist()

# 4.1 Define ordinal categories for KitchenQual (typical Ames order)
# Check if KitchenQual is in categorical_cols before defining the order
kitchen_qual_order = ['Po', 'Fa', 'TA', 'Gd', 'Ex']
ordinal_features = []
if 'KitchenQual' in categorical_cols:
    ordinal_mappings = {'KitchenQual': kitchen_qual_order}
    ordinal_features.append('KitchenQual')
    categorical_cols.remove('KitchenQual') # Remove from categorical to avoid double processing

# For nominal categoricals: all remaining categorical columns
nominal_cols = categorical_cols

# 4.2 Missing value imputation strategies (justified)
# Numeric: median imputation (robust to outliers)
# Ordinal: most frequent (mode) because missing is rare; treat as 'typical' quality
# Nominal: most frequent (mode) – missing values are rare and imputing with mode preserves distribution
numeric_imputer = SimpleImputer(strategy='median')
ordinal_imputer = SimpleImputer(strategy='most_frequent') # Used for KitchenQual if it exists
nominal_imputer = SimpleImputer(strategy='most_frequent')

# 4.3 Scaling: StandardScaler for numeric features (tree models don't need it, but good practice for consistency)
scaler = StandardScaler()

# 4.4 Encoding:
# Ordinal: use OrdinalEncoder with custom categories
# Only create ordinal_encoder if ordinal_features is not empty
if ordinal_features:
    ordinal_encoder = OrdinalEncoder(categories=[ordinal_mappings['KitchenQual']],
                                      handle_unknown='use_encoded_value', unknown_value=-1)
else:
    ordinal_encoder = None # Or handle this case appropriately

# Nominal: OneHotEncoder (drop first to avoid multicollinearity)
nominal_encoder = OneHotEncoder(drop='first', sparse_output=False, handle_unknown='ignore')

# 4.5 Build ColumnTransformer
# Only include ordinal processing if ordinal_features is not empty
transformers = [
    ('num', Pipeline([('impute', numeric_imputer), ('scale', scaler)]), numeric_cols)
]
if ordinal_features and ordinal_encoder is not None:
    transformers.append(('ord', Pipeline([('impute', ordinal_imputer), ('encode', ordinal_encoder)]), ordinal_features))
if nominal_cols:
    transformers.append(('nom', Pipeline([('impute', nominal_imputer), ('encode', nominal_encoder)]), nominal_cols))

preprocessor = ColumnTransformer(transformers, remainder='passthrough') # 'passthrough' for columns not explicitly transformed

# Test preprocessor on train (no leakage)
X_train_processed = preprocessor.fit_transform(X_train)
print(f"\nProcessed training data shape: {X_train_processed.shape}")

# -----------------------------------------------------------------------------
# 5. MODEL PIPELINES (TWO SWAPPABLE MODELS)
# -----------------------------------------------------------------------------
# Model 1: Random Forest
rf_pipeline = Pipeline([
    ('preprocessor', preprocessor),
    ('regressor', RandomForestRegressor(random_state=42, n_jobs=-1))
])

# Model 2: Gradient Boosting
gb_pipeline = Pipeline([
    ('preprocessor', preprocessor),
    ('regressor', GradientBoostingRegressor(random_state=42))
])

# -----------------------------------------------------------------------------
# 6. HYPERPARAMETER TUNING (USING TRAIN + VALIDATION, BUT CV ON TRAIN)
# -----------------------------------------------------------------------------
# To avoid overfitting the validation set, we perform cross-validation on the training set
# and then evaluate each tuned model on the validation set. We'll choose the best model based on validation RMSE.

# Parameter grids
param_grid_rf = {
    'regressor__n_estimators': [100, 200],
    'regressor__max_depth': [10, 20, None],
    'regressor__min_samples_split': [2, 5]
}

param_grid_gb = {
    'regressor__n_estimators': [100, 200],
    'regressor__learning_rate': [0.05, 0.1],
    'regressor__max_depth': [3, 5]
}

# Grid search with 3-fold CV on training set
grid_rf = GridSearchCV(rf_pipeline, param_grid_rf, cv=3, scoring='neg_mean_squared_error',
                       n_jobs=-1, verbose=1)
grid_gb = GridSearchCV(gb_pipeline, param_grid_gb, cv=3, scoring='neg_mean_squared_error',
                       n_jobs=-1, verbose=1)

print("\nTraining Random Forest with GridSearchCV...")
grid_rf.fit(X_train, y_train)

print("\nTraining Gradient Boosting with GridSearchCV...")
grid_gb.fit(X_train, y_train)

# Best estimators
best_rf = grid_rf.best_estimator_
best_gb = grid_gb.best_estimator_

print("\nBest Random Forest params:", grid_rf.best_params_)
print("Best Gradient Boosting params:", grid_gb.best_params_)

# -----------------------------------------------------------------------------
# 7. EVALUATION ON VALIDATION SET (MODEL COMPARISON)
# -----------------------------------------------------------------------------
def evaluate_model(model, X, y, name="Model"):
    y_pred = model.predict(X)
    rmse = np.sqrt(mean_squared_error(y, y_pred))
    mae = mean_absolute_error(y, y_pred)
    r2 = r2_score(y, y_pred)
    print(f"{name} - RMSE: ${rmse:,.0f}, MAE: ${mae:,.0f}, R2: {r2:.4f}")
    return rmse, mae, r2

print("\n--- Validation Set Performance ---")
evaluate_model(best_rf, X_val, y_val, "Random Forest (tuned)")
evaluate_model(best_gb, X_val, y_val, "Gradient Boosting (tuned)")

# Choose best model based on validation RMSE
val_rf_rmse = np.sqrt(mean_squared_error(y_val, best_rf.predict(X_val)))
val_gb_rmse = np.sqrt(mean_squared_error(y_val, best_gb.predict(X_val)))

if val_rf_rmse < val_gb_rmse:
    best_model = best_rf
    best_model_name = "Random Forest"
else:
    best_model = best_gb
    best_model_name = "Gradient Boosting"

print(f"\nBest model on validation: {best_model_name} (RMSE: ${min(val_rf_rmse, val_gb_rmse):,.0f})")

# -----------------------------------------------------------------------------
